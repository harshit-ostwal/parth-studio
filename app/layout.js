"use client"
import Navbar from '@/components/Navbar'
import './globals.css'
import Footer from '@/components/Footer'
import BottomToTop from '@/components/BottomToTop'
import { Varela_Round, Oregano, Salsa } from 'next/font/google'
import { usePathname } from 'next/navigation'
import FilmsData from '@/components/Data/Films'

const oregano = Oregano({ subsets: ["latin"], variable: "--oregano", weight: "400" });
const varela = Varela_Round({ subsets: ["latin"], variable: "--varela", weight: "400" });
const salsa = Salsa({ subsets: ["latin"], variable: "--salsa", weight: "400" });

// export const metadata = {
//   title: 'Parth Studio',
//   description: 'Generated by create next app',
// }

export default function RootLayout({ children }) {

  const pathname = usePathname();

  const isFilmPath = FilmsData.some((data) => data.href === pathname);


  return (
    <html lang="en">
      <body className={`${oregano.variable} ${varela.variable} ${salsa.variable}`}>
        {isFilmPath ? (
          <>
            <div className="bg-slate-950 h-screen overflow-hidden">
              {children}
            </div>
          </>
        ) : (
          <>
            <div className="bg-white h-screen flex flex-col overflow-y-auto overflow-hidden">
              <Navbar />
              <div className="flex-1">
                {children}
              </div>
              <Footer />
              <BottomToTop />
            </div>
          </>
        )}
      </body>
    </html >
  )
}
